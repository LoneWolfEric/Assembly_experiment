DSEG	SEGMENT
VAR 	DB 5, 7, 19H, 23H, 0A0H
N	EQU $ -VAR
DSEG	ENDS
CSEG	SEGMENT
	ASSUME CS:CSEG,DS:DSEG
BG:	MOV AX, DSEG
	MOV DS, AX
	MOV CX, N-1
	MOV SI, 0
	MOV BH, VAR[SI]
	JCXZ LAST
AGIN:	INC SI          ;si前移，换下一个数字
	CMP BH, VAR[SI]
	JAE NEXT            ;bh比新数字大就不存了
	MOV BH, VAR[SI]
NEXT:	LOOP AGIN
LAST:	MOV AH, 4CH
	INT 21H
CSEG	ENDS
	END BG